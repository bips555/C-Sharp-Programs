using System;
namespace GreaterUsingStaticMathExample{
    public class GreaterUsingStaticMathExample{
        public static void Main(string[] args){
            int FirstNo = 600;
            int SecondNo = 500;
            int GreaterNo = Math.Max(FirstNo,SecondNo);
            Console.WriteLine(GreaterNo);
        }
    }
}